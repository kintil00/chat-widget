    function generateUUID() {
        return crypto.randomUUID();
    }

    function getFormattedTime() {
        const now = new Date();
        const hours = String(now.getHours()).padStart(2, '0');
        const minutes = String(now.getMinutes()).padStart(2, '0');
        return `${hours}:${minutes}`;
    }


    async function startNewConversation() {
        currentSessionId = generateUUID();
        const data = [{
            action: "loadPreviousSession",
            sessionId: currentSessionId,
            route: config.webhook.route,
            metadata: {
                userId: ""
            }
        }];

        try {
            const response = await fetch(config.webhook.url, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify(data)
            });

            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            const responseData = await response.json();
            // New Conversation elements are removed, no need to hide them
            // chatContainer.querySelector('.brand-header').style.display = 'none';
            // chatContainer.querySelector('.new-conversation').style.display = 'none';
            // chatInterface.classList.add('active'); // Already active in HTML

            if (responseData && responseData.output) {
                const botMessageDiv = document.createElement('div');
                botMessageDiv.className = 'chat-message bot';
                botMessageDiv.textContent = responseData.output;

                const timestampSpan = document.createElement('span');
                timestampSpan.className = 'timestamp';
                timestampSpan.textContent = getFormattedTime();
                botMessageDiv.appendChild(timestampSpan);

                messagesContainer.appendChild(botMessageDiv);
                messagesContainer.scrollTop = messagesContainer.scrollHeight;
            }


        } catch (error) {
            console.error('Error starting conversation:', error);
            // Display error message to the user in chat?
            const errorDiv = document.createElement('div');
            errorDiv.className = 'chat-message bot error';
            errorDiv.textContent = 'Failed to start conversation. Please try again later.';
            messagesContainer.appendChild(errorDiv);
            chatInterface.classList.add('active'); // Ensure chat interface is shown even on error
        }
    }

    async function sendMessage(message) {
        if (!message.trim()) return;

        const messageData = {
            action: "sendMessage",
            sessionId: currentSessionId,
            route: config.webhook.route,
            chatInput: message,
            metadata: {
                userId: ""
            }
        };

        // Display user message immediately
        const userMessageDiv = document.createElement('div');
        userMessageDiv.className = 'chat-message user';
        userMessageDiv.textContent = message;
        const timestampSpanUser = document.createElement('span');
        timestampSpanUser.className = 'timestamp';
        timestampSpanUser.textContent = getFormattedTime();
        userMessageDiv.appendChild(timestampSpanUser);


        messagesContainer.appendChild(userMessageDiv);
        messagesContainer.scrollTop = messagesContainer.scrollHeight;


        try {
            const response = await fetch(config.webhook.url, {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json'
                },
                body: JSON.stringify(messageData)
            });

            if (!response.ok) {
                throw new Error(`HTTP error! status: ${response.status}`);
            }

            const data = await response.json();


            if (data && data.output) {
                const botMessageDiv = document.createElement('div');
                botMessageDiv.className = 'chat-message bot';
                botMessageDiv.textContent = data.output;

                const timestampSpanBot = document.createElement('span');
                timestampSpanBot.className = 'timestamp';
                timestampSpanBot.textContent = getFormattedTime();
                botMessageDiv.appendChild(timestampSpanBot);


                messagesContainer.appendChild(botMessageDiv);
                messagesContainer.scrollTop = messagesContainer.scrollHeight;
            }


        } catch (error) {
            console.error('Error sending message:', error);
            // Display error message in chat
            const errorDiv = document.createElement('div');
            errorDiv.className = 'chat-message bot error';
            errorDiv.textContent = 'Failed to send message. Please try again.';
            messagesContainer.appendChild(errorDiv);
        }
    }

    // Start new conversation immediately when widget is initialized
    startNewConversation();

    sendButton.addEventListener('click', () => {
        sendMessage(textarea.value);
        textarea.value = '';
        textarea.style.height = 'auto'; // Reset textarea height
    });

    textarea.addEventListener('keypress', (e) => {
        if (e.key === 'Enter' && !e.shiftKey) {
            e.preventDefault();
            sendMessage(textarea.value);
            textarea.value = '';
            textarea.style.height = 'auto'; // Reset textarea height
        }
    });

    textarea.addEventListener('input', () => {
        textarea.style.height = 'auto';
        textarea.style.height = `${textarea.scrollHeight}px`; // Adjust textarea height based on content
    });


    toggleButton.addEventListener('click', () => {
        chatContainer.classList.toggle('open');
    });

